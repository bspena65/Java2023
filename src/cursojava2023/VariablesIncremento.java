public class VariablesIncremento {

    public static void main(String[] args) {

        var incremento = 1;

        System.out.println(incremento);
        incremento++;
        incremento++;
        incremento++;
      
        System.out.println(incremento);

        var decremento = 10;

        System.out.println(decremento);
        decremento--;
        decremento--;
        decremento--;
      
     
        System.out.println(decremento);
        
        
        var i =20;
        i++;
        i--;
        i++;
        i++;
        
        System.out.println(i);
    }
    
}
